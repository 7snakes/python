items_list = []
user_input = ""
while user_input != "q":
    print("Введите 'название':")
    item_name = input()
    print("Введите 'цену':")
    item_price = input()
    print("Введите 'количество':")
    item_quantity = input()
    print("Введите 'единицы измерения':")
    item_unit = input()
    items_list.append((len(items_list) + 1, {"название": item_name, "цена": float(item_price), "количество": int(item_quantity), "eд": item_unit}))
    print("Для окончания ввода данных введите 'q' или любое другое значение для ввода данных о следующем товаре")
    user_input = input()

print(items_list)

table = {}
for item in items_list:
    for key, value in item[1].items():
        if table.get(key) is None:
            table[key] = set()
        table[key].add(value)
print("Аналитика: {}".format(table))
