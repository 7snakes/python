# 2. Для списка реализовать обмен значений соседних элементов, т.е. Значениями обмениваются элементы с индексами 0 и 1, 2 и 3 и т.д. При нечетном количестве элементов последний сохранить на своем месте.
#    Для заполнения списка элементов необходимо использовать функцию input().
user_input = ""
user_input_list = []
while user_input != "\\q":
    print("input new item or \\q to quit")
    user_input = input()
    if user_input != "\\q":
        user_input_list.append(user_input)

for i in range(0, len(user_input_list), 2):
    if i != len(user_input_list) - 1:
        user_input_list[i], user_input_list[i + 1] = user_input_list[i + 1], user_input_list[i]

print("Items: {}".format(user_input_list))

# 3. Пользователь вводит месяц в виде целого числа от 1 до 12.
# Сообщить к какому времени года относится месяц (зима, весна, лето, осень). Напишите решения через list и через dict.
month_list = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"]
month_dict = dict(zip(range(len(month_list)), month_list))
print("Enter month:")
user_month = input()
month_num = int(user_month)
if 1 < month_num < 12:
    print("Month list: {}".format(month_list[month_num]))
    print("Month dict: {}".format(month_dict.get(month_num)))

